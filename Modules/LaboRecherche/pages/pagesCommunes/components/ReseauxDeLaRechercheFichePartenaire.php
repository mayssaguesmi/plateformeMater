            if (!clean.length) return [];

            // 1) Essaye un batch: /projet?ids=1,2,3 (adapte le param si ton API utilise include=)
            try {
                const res = await api('projet', {
                    method: 'GET',
                    query: {
                        ids: clean.join(','),
                        per_page: 200
                    }
                });
                const arr = Array.isArray(res) ? res : (Array.isArray(res?.items) ? res.items : []);
                if (arr.length) {
                    // Filtrer encore côté JS pour être sûr de n’avoir que ces IDs (au cas où l’API ignore le filtre)
                    const set = new Set(clean);
                    return arr.filter(p => set.has(parseInt(p.id, 10)));
