            method = "GET"
        } = {}) {
            const url = `${CFG.base}/${CFG.ns}/${path}`;
            const opt = {
                method,
                headers: {
                    "X-WP-Nonce": CFG.nonce,
                    "Accept": "application/json"
                },
                credentials: "include"
            };
            const r = await fetch(url, opt);
            const t = await r.text();
            let j;
            try {
                j = JSON.parse(t);
            } catch {
                j = {
                    raw: t
                };
            }
            if (!r.ok) throw new Error(j?.message || `HTTP ${r.status}`);
            return j;
